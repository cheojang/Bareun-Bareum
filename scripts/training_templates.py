def get_template_key(phoneme, position, error_type, error_category):
    """가장 적합한 템플릿 키 반환"""
    # 직접 매칭
    for key in [(phoneme, error_type), (phoneme, error_type.split('+')[0])]:
        if key in STEP_TEMPLATES:
            return key

    # 어중/종성 특수 케이스
    if position == '어중' and '비음화' in error_type and (phoneme, '어중_비음화') in STEP_TEMPLATES:
        return (phoneme, '어중_비음화')
    if position == '종성' and '탈락' in error_type and (phoneme, '종성_탈락') in STEP_TEMPLATES:
        return (phoneme, '종성_탈락')
    if position == '종성' and '탈락' in error_type:
        return ('종성', '탈락')

    # 동화 카테고리
    if '동화' in error_category or '동화' in error_type:
        return ('동화', '순음화동화')

    # 오류유형별 fallback
    for etype in ['탈락', '경음화', '기음화', '비음화', '파열음화', '마찰음화', '파찰음화', '유음화', '순음화']:
        if etype in error_type:
            candidate = (phoneme, etype)
            if candidate in STEP_TEMPLATES:
                return candidate

    return None


